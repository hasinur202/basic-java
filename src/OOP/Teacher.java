package OOP;

public class Teacher {

    String name;
    String gender;
    int age;

    Teacher(String n, String g, int p) {
        name = n;
        gender = g;
        age = p;

    }

    void DisInfo() {

        System.out.println("Name: " + name);
        System.out.println("Name: " + gender);
        System.out.println("Name: " + age);
        System.out.println("");

    }

}
